public class Dav5 {
    public static void main(String[] args) {
        double x = 2;
        double y = 4;
        char n = '*';
        switch (n) {
            case '+':
                System.out.println(x+y);
                break;
            case '-':
                System.out.println(x-y);
                break;
            case '*':
                System.out.println(x*y);
                break;
            case '/':
                System.out.println(x/y);
                break;
        }
    }
}
